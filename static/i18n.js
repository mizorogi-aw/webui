  function normalizeLanguage(value) {
    const normalized = String(value || "").toLowerCase();
    return SUPPORTED_LANGUAGES.includes(normalized) ? normalized : DEFAULT_LANGUAGE;
  }

  function getLanguage() {
    try {
      return normalizeLanguage(window.localStorage.getItem(STORAGE_KEY));
    } catch (_error) {
      return DEFAULT_LANGUAGE;
    }
  }

  function setLanguage(language) {
    const normalized = normalizeLanguage(language);
    try {
      window.localStorage.setItem(STORAGE_KEY, normalized);
    } catch (_error) {
      // ignore storage failures
    }
    document.documentElement.lang = normalized;
    return normalized;
  }

  function format(template, params) {
    return String(template).replace(/\{([^}]+)\}/g, (_match, key) => {
      return Object.prototype.hasOwnProperty.call(params || {}, key) ? String(params[key]) : "";
    });
  }

  function t(key, params = {}) {
    const language = getLanguage();
    const dictionary = translations[language] || translations.en;
    const fallback = translations.en || {};
    const template = Object.prototype.hasOwnProperty.call(dictionary, key)
      ? dictionary[key]
      : fallback[key] || key;
    return format(template, params);
  }

  function toggleLanguage() {
    return setLanguage(getLanguage() === "ja" ? "en" : "ja");
  }

  function applyTranslations(root = document) {
    for (const element of root.querySelectorAll("[data-i18n]")) {
      const key = element.getAttribute("data-i18n");
      const attr = element.getAttribute("data-i18n-attr");
      const translated = t(key);
      if (attr) {
        element.setAttribute(attr, translated);
      } else if (element.getAttribute("data-i18n-html") === "true") {
        element.innerHTML = translated;
      } else {
        element.textContent = translated;
      }
    }
  }

  setLanguage(getLanguage());

  window.FieldGatewayI18n = {
    STORAGE_KEY,
    SUPPORTED_LANGUAGES,
    getLanguage,
    setLanguage,
    toggleLanguage,
    applyTranslations,
    normalizeLanguage,
    t,
  };
